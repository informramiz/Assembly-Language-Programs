.MODEL SMALL
.STACK 100H
.DATA
	INPUT_MSG DB		0AH,0DH,'Enter a character: $'
	BINARY_OUTPUT DB	0AH,0DH,'Its binary is: $'
	HEX_OUTPUT DB		'  and its hex is: $'
.CODE

MAIN PROC
	
	MOV AX,@DATA
	MOV DS,AX
	
INPUT:
	LEA DX,INPUT_MSG
	MOV AH,9
	INT 21H
	
	MOV AH,1
	INT 21H
	
	CMP AL,0DH
	JE EXIT

	MOV BL,AL
	MOV BH,AL
	
	LEA DX,BINARY_OUTPUT
	MOV AH,9
	INT 21H
	
	MOV AH,2
	MOV CX,8

TOP:
	RCL BL,1
	JC ONE
	MOV DL,'0'
	JMP NEXT
ONE:
	MOV DL,'1'
	
NEXT:
	INT 21h
	LOOP TOP

HEx:
	LEA DX,HEX_OUTPUT
	MOV AH,9
	INT 21H

	MOV CL,4
	MOV CH,2
	MOV AH,2
	
PRINT:
	MOV DL,BH
	SHR DL,CL
	
	CMP DL,9H
	JG LETTER
	OR DL,30H
	JMP DISPLAY

LETTER:
	ADD DL,37H

DISPLAY:
	INT 21H
	
	ROL BH,CL
	DEC CH
	JNZ PRINT
BACK:
	JMP INPUT
	
EXIT:
	MOV AH,4CH
	INT 21H

MAIN ENDP
	END MAIN