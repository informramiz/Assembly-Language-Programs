 .MODEL SMALL
 .STACK 100H
 .DATA
	ARRAY1 DB 'a','b','c','d','e','f','g','h'
	ARRAY2 DB 0H,0H,0H,0H,0H,0H,0H,0H
 .CODE

 MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV CX,8
COPY:
	LEA si,ARRAY1
	MOV AH,[si]
	LEA di,ARRAY2
	MOV [di],AH
	
	INC si
	INC di
	DEC CX
	JNZ COPY

	MOV AH,2
	MOV CX,16
	MOV DL,ARRAY1
DISPLAY:
	INT 21H
	INC ARRAY1
	MOV DL,ARRAY1
	DEC CX
	JNZ DISPLAY

EXIT:
	MOV AH,4CH
	INT 21H	
	
MAIN ENDP
	END MAIN
	
	

