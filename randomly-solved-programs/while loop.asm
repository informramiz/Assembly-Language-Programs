 .MODEL SMALL
 .STACK 100H
 .DATA
	MSG1 DB 'ENTER A NUMBER IN HEX (A-F) : $'
	MSG2 DB 0DH,0AH,'NUMBER IN DECIMAL IS: $'
 .CODE

 MAIN PROC
	MOV DX,0
	MOV AH,1
	INT 21H
	
WHILE_:
	CMP AL,0DH
	JE EXIT
	
	INC DX
	INT 21H
	JMP WHILE_
	

 EXIT:

	MOV AH,4CH
	INT 21H	
	
 MAIN ENDP
	END MAIN
	
	

