OPTION1 PROC	;use al and call appropriate proc

	PUSH DX
	PUSH AX
	
START:
	LEA DX,INPUT_MSG
	MOV AH,9
	INT 21H
	
	LEA DI,STRING
	CALL INPUT
	
	LEA SI,STRING
		
	MOV AH , 9
	LEA DX,BUBBLE_OPTION
	INT 21H
	
	LEA DX,SELECTION_OPTION
	INT 21H
	
	LEA DX,INSERTION_OPTION
	INT 21H
		
	LEA DX,GNUM_OPTION
	INT 21H
	
	LEA DX,MINIMUM_OPTION
	INT 21H

	LEA DX,OPTION_INPUT
	INT 21H
	
	MOV AH,1
	INT 21H
	
	CMP AL,'5'
	JG INVALID
	
	CMP AL,'1'
	JE ONE
	
	CMP AL,'2'
	JE TWO
	
	CMP AL,'3'
	JE THREE
	
	CMP AL,'4'
	JE FOUR
	
	CMP AL,'5'
	JE FIVE
	
	INVALID:
		LEA DX,INVALID_OPTION
		MOV AH,9
		INT 21H
		JMP START
	
	ONE:
		CALL BUBBLE_SORT
		JMP AGAIN1
		
	TWO:
		CALL SELECT_SORT
		JMP AGAIN1
		
	THREE:
		CALL INSERT_SORT
		JMP AGAIN1
			
	FOUR:
		CALL GNUM_SORT
		JMP AGAIN1
		
	FIVE:
		CALL MINIMUM_ELEMENT_SORT
		JMP AGAIN1
	
	AGAIN1:
		LEA DX,OUTPUT_MSG
		MOV AH,9
		INT 21H
		
		CALL OUTPUT
		
		LEA DX,INPUT_AGAIN
		MOV AH,9
		INT 21H
		
		MOV AH,1
		INT 21H
		
		CMP AL,'1'
		JE START
		
	RETURN2:	
		POP AX
		POP DX
	RET

OPTION1 ENDP